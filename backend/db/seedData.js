// Enrich products with joined fields (mirrors DB JOIN result)
function getEnrichedProducts() {
  const catMap = Object.fromEntries(categories.map(c => [c.id, c.label]))
  const mfrMap = Object.fromEntries(manufacturers.map(m => [m.id, m]))
  return products.map((p, i) => ({
    id: -(i + 1), // negative ids to distinguish from DB rows
    is_on_sale: false,
    created_at: null,
    updated_at: null,
    spec_columns: null,
    ...p,
    category_label:      catMap[p.category_id]      || null,
    manufacturer_label:  mfrMap[p.manufacturer_id]?.label   || null,
    country:             mfrMap[p.manufacturer_id]?.country  || null,
  }))
}

module.exports = { categories, manufacturers, products, getEnrichedProducts }
